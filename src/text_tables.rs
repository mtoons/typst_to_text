pub fn superscript(string: String) -> String {
    let mut result: String = Default::default();
    for c in string.chars() {
        let r: char = c_superscript(c);
        if r == '\0' {
            return format!("^({})", string);
        } else {
            result.push(r);
        }
    }
    result
}
