pub fn subscript(string: String) -> String {
    let mut result: String = Default::default();
    for c in string.chars() {
        let r: char = c_subscript(c);
        if r == '\0' {
            return format!("_{}", string);
        } else {
            result.push(r);
        }
    }
    result
}
